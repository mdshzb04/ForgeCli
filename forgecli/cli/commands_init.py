# Internal helpers
# ---------------------------------------------------------------------------

_PROVIDER_KEYS: list[tuple[str, str]] = [
    ("openai", "OPENAI_API_KEY"),
    ("anthropic", "ANTHROPIC_API_KEY"),
    ("google", "GOOGLE_API_KEY"),
    ("google", "GEMINI_API_KEY"),
]


def _detect_providers() -> list[str]:
    """Return the names of providers whose API key is set in the environment."""
    seen: set[str] = set()
    result: list[str] = []
    for provider, env_var in _PROVIDER_KEYS:
        if provider in seen:
            continue
        if os.environ.get(env_var):
            result.append(provider)
            seen.add(provider)
    return result


def _run_doctor_check() -> dict[str, Any]:
    """Run the doctor dependency check and return a summary dict."""
    try:
        from forgecli.platform import check_dependencies, current_platform, python_version

        platform = current_platform()
        report = check_dependencies()
        return {
            "platform": platform.os.value,
            "python": python_version(),
            "all_ok": len(report.missing_required) == 0,
            "missing_required": [d.name for d in report.missing_required],
            "missing_optional": [
                d.name for d in report.missing if not d.required
            ],
        }
    except Exception as exc:
        return {"error": str(exc), "all_ok": False}


def _render_doctor_summary(console, report: dict[str, Any]) -> None:
    """Print a compact doctor summary."""
    if report.get("error"):
        warn(f"Doctor check failed: {report['error']}")
        return

    if report.get("all_ok"):
        success(
            f"Self-check passed "
            f"(Python {report.get('python', '?')}, "
            f"{report.get('platform', '?')})"
        )
    else:
        missing = report.get("missing_required", [])
        error(f"Missing required dependencies: {', '.join(missing)}")

    optional_missing = report.get("missing_optional", [])
    if optional_missing:
        # Filter out ponytail from "missing" since it's built-in
        optional_missing = [d for d in optional_missing if d != "ponytail"]
        if optional_missing:
            info(f"Optional tools not found: {', '.join(optional_missing)}")


__all__ = ["app"]
